double soma(double a, double b) {
	return a+b;
}

double mult(double a, double b) {
	return a*b;
}

double div(double a, double b) {
	return a/b;
}
double sub(double a, double b) {
	return a-b;
}
double pot(double a, double b){
	return pow(a,b);
}
double e(double a){
	return pow(2.718281828459045235360287471352662497757,a);
}


